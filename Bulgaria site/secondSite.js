function toggle(){
    var dropdownM = document.getElementById("dropDownMenu" );
    if (dropdownM.style.display == "none") {
        dropdownM.style.display = "block";
    } else {
        dropdownM.style.display = "none";
    }        
}
function toggleSecond(){
    var dropdownMSecond = document.getElementById("dropDownMenuSecond" );
    if (dropdownMSecond.style.display == "none") {
        dropdownMSecond.style.display = "block";
    } else {
        dropdownMSecond.style.display = "none";
    }        
}